const state = {
  contractAddress: "0xb024dE86C5D959ddC55ED0b1f6DA21103879C3bA",
  contractAbi: ABI,
  provider: new ethers.providers.Web3Provider(window.ethereum),
  signer: null,
  contract: null,

  async connectWallet() {
    // DOM elements
    const connectImageButtonElement = document.getElementById('connect-img-btn');
    const generateImageButtonElement = document.getElementById('generate-img-btn');
    connectImageButtonElement.classList.add('hidden')
    generateImageButtonElement.classList.remove('hidden')
    await state.provider.send("eth_requestAccounts", []);
    state.signer = state.provider.getSigner();
    state.contract = new ethers.Contract(
      state.contractAddress,
      state.contractAbi,
      state.signer
    );
  },

  async testConnection() {
    const userAddress = await state.signer.getAddress();
    const balance = await state.provider.getBalance(userAddress);
    console.log({ userAddress, balance: ethers.utils.formatEther(balance) });
    console.log({ uri: await state.contract.tokenURI(0) });
  },

  async mintToken(metadataURI) {
    const userAddress = await state.signer.getAddress();
    const price = await state.contract.getPrice();
    const tx = await state.contract.payToMint(userAddress, metadataURI, {
      value: price,
    });
    await tx.wait();
    console.log("isContentOwned", await state.contract.isContentOwned(metadataURI));
    console.log("count", await state.contract.count());
  },

  async setImgUri(tokenId, imgId) {
    const uri = await state.contract.tokenURI(tokenId);
    const ipfsCID = uri.slice(7); // skip 'ipfs://'
    const imageURI = `https://gateway.pinata.cloud/ipfs/${ipfsCID}/genLineArt.jpeg`;
    const img = document.getElementById(imgId);
    img.src = imageURI;
  },
};
window.walletState = state;
